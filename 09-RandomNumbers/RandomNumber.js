// SECTION - Random Number generation in JavaScript

// to get decimal number between 0 to 9.99
let randomNumber=Math.random()*10;
console.log(randomNumber);

// to get int number between 0 to 9
let randomNumber2=Math.random()*10;
console.log(Math.floor(randomNumber2));

// To get a int number between 0 to 10
let randomNumber3=Math.random()*10+1;
console.log(Math.floor(randomNumber3));

